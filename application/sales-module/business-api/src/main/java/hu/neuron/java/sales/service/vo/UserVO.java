package hu.neuron.java.sales.service.vo;

import java.io.Serializable;
import java.util.UUID;

public class UserVO implements Serializable {

	private static final long serialVersionUID = -1465616249735667174L;

	private Long id;

	private String name;

	private String userId;

	private String userName;

	private String password;

	private String email;

	private String phoneNumber;
	
	private SalesPointVO salesPoint;
	
	public UserVO() {
		this.userId = UUID.randomUUID().toString();
	}
	
	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getPhoneNumber() {
		return phoneNumber;
	}

	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}

	@Override
	public String toString() {
		return "UserVO [name=" + name + ", userName=" + userName + ", email=" + email + "]";
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((userId == null) ? 0 : userId.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		UserVO other = (UserVO) obj;
		if (userId == null) {
			if (other.userId != null)
				return false;
		} else if (!userId.equals(other.userId))
			return false;
		return true;
	}

	public SalesPointVO getSalesPoint() {
		return salesPoint;
	}

	public void setSalesPoint(SalesPointVO salesPoint) {
		this.salesPoint = salesPoint;
	}	
}
